public class wrapper {
    public static void main(String[] args) {
        // Wrapper class = Provides a way to use primitive datatypes as reference datatypes, refrence datatypes contains useful methods i.e. it can be used with collections.


        // Primitive          Wrapper
        // - boolean         -Boolean
        // - char            -Character
        // - int             -Integer
        // - double          -Double


        // Autoboxing = The automatic conversion that Java compiler makes between primitive data types and their corresponding object wrapper classes.
        // Unboxing = The reverse of autoboxing. Automatic conversion of wrapper class to primitive data types.

        // These are wrapper classes.
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Aditya";
    }
}
